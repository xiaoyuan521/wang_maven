package com.mvc.model;

public class GameCountModel {

    private String id = null;
    private String inforId = null;
    private String allGamesCount = null;
    private String successCount = null;
    private String inforName = null;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getInforId() {
        return inforId;
    }

    public void setInforId(String inforId) {
        this.inforId = inforId;
    }

    public String getAllGamesCount() {
        return allGamesCount;
    }

    public void setAllGamesCount(String allGamesCount) {
        this.allGamesCount = allGamesCount;
    }

    public String getSuccessCount() {
        return successCount;
    }

    public void setSuccessCount(String successCount) {
        this.successCount = successCount;
    }

    public String getInforName() {
        return inforName;
    }

    public void setInforName(String inforName) {
        this.inforName = inforName;
    }

}
